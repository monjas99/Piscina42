#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;

	i = 0;
	while (argc > 1)
	{
		i = 0;
		while (argv[argc - 1][i])
		{
			write (1, &argv[argc - 1][i], 1);
			i++;
		}
		write (1, "\n", 1);
		argc--;
	}	
	return (0);
}
